/**
 * PATCH /api/incidents/:id/add-logs
 * Attach more logs to incident
 */
export const addLogs = async (req, res) => {
  try {
    const { id } = req.params;
    const { log_ids } = req.body;

    if (!Array.isArray(log_ids) || log_ids.length === 0) {
      return res.status(400).json({
        success: false,
        error: 'log_ids must be a non-empty array'
      });
    }

    const incident = await Incident.findById(id);
    if (!incident) {
      return res.status(404).json({
        success: false,
        error: 'Incident not found'
      });
    }

    // Add logs (avoid duplicates)
    const existingIds = incident.related_log_ids.map(id => id.toString());
    const newLogIds = log_ids.filter(logId => !existingIds.includes(logId.toString()));

    if (newLogIds.length > 0) {
      incident.related_log_ids.push(...newLogIds);

      // Add timeline entry
      addTimelineEntry(
        incident,
        'evidence_added',
        `${newLogIds.length} log(s) added as evidence`,
        req.user.id,
        { log_count: newLogIds.length }
      );

      incident.updated_at = new Date();
      await incident.save();
    }

    res.json({
      success: true,
      message: `${newLogIds.length} log(s) linked to incident`,
      incident
    });
  } catch (error) {
    console.error('‚ùå Error adding logs:', error);
    res.status(500).json({
      success: false,
      error: 'Failed to add logs',
      message: error.message
    });
  }
};

/**
 * GET /api/incidents/stats
 * Get incident statistics
 */
export const getIncidentStats = async (req, res) => {
  try {
    const now = new Date();
    const last7d = new Date(now - 7 * 24 * 60 * 60 * 1000);

    const [
      totalIncidents,
      openIncidents,
      criticalIncidents,
      severityStats,
      statusStats,
      recentIncidents
    ] = await Promise.all([
      Incident.countDocuments(),
      Incident.countDocuments({ status: 'open' }),
      Incident.countDocuments({ severity: 'critical', status: { $nin: ['resolved', 'closed'] } }),
      Incident.aggregate([
        { $group: { _id: '$severity', count: { $sum: 1 } } }
      ]),
      Incident.aggregate([
        { $group: { _id: '$status', count: { $sum: 1 } } }
      ]),
      Incident.find({ created_at: { $gte: last7d } }).countDocuments()
    ]);

    res.json({
      success: true,
      stats: {
        total_incidents: totalIncidents,
        open_incidents: openIncidents,
        critical_incidents: criticalIncidents,
        recent_incidents_7d: recentIncidents,
        severity_distribution: severityStats.reduce((acc, s) => {
          acc[s._id] = s.count;
          return acc;
        }, {}),
        status_distribution: statusStats.reduce((acc, s) => {
          acc[s._id] = s.count;
          return acc;
        }, {})
      }
    });
  } catch (error) {
    console.error('‚ùå Error fetching incident stats:', error);
    res.status(500).json({
      success: false,
      error: 'Failed to fetch statistics',
      message: error.message
    });
  }
};

